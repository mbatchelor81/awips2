    /**
     * Handles a file being waited on at the recipient site.
     */
    /*
     * NOTE: Demo this functionality to Client and walk-through its Use-Cases to
     * determine its importance to the WorkFlow.
     */
    protected void handleWait() {
        BackupSvcIncomingResponse response = null;
        IncomingBackupJobsRequest request = null;
        List<Long> jobIDs = new ArrayList<>();
        String jobStatus;
        List<Long> outJobIDs = new ArrayList<>();
        // get all the incoming and outgoing job id's and store it into a list
        for (TableItem selectedTableItem : getSelectedTableItems()) {

            jobStatus = ((BackupSvcIncoming) selectedTableItem.getData())
                    .getStatus();
            // if a job has not been Accepted or Rejected mark it with Wait
            // status
            if (jobStatus.equalsIgnoreCase(DbStatus.NEW.toString())
                    || jobStatus.equalsIgnoreCase(DbStatus.WAIT.toString())
                    || jobStatus.equalsIgnoreCase(DbStatus.FAILED.toString())) {
                jobIDs.add(((BackupSvcIncoming) selectedTableItem.getData())
                        .getBksvcId());
                outJobIDs.add(((BackupSvcIncoming) selectedTableItem.getData())
                        .getOutBksvcId());
            }
        }
        /*
         * If items are not selected don't make a request to server, instead
         * prompt the user to make selections
         */
        if (!(jobIDs.isEmpty() && outJobIDs.isEmpty())) {
            request = new IncomingBackupJobsRequest(
                    IncomingOutgoingJobsRequest.RequestType.WAIT,
                    LocalizationManager.getInstance().getSite(), jobIDs,
                    outJobIDs);
            try {
                response = (BackupSvcIncomingResponse) RequestRouter
                        .route(request);
            } catch (Exception e) {
                statusHandler.error(
                        "Unable to send request to server to Wait on Incoming Backup Jobs.",
                        e);
            }
            // Update statusMsgText
            StringBuilder sb = new StringBuilder();
            if (response == null) {
                sb.append("The following Backup Jobs with ID(s) " + jobIDs
                        + " failed to be Waited on. Null Response From Edex");
            }
            /*
             * Check the response object passed to CAVE for exceptions and
             * IncomingJob list is not empty
             */
            else if ((response.getExceptions() == null
                    || response.getExceptions().getMessage().isEmpty())
                    && (!response.getIncomingJobs().isEmpty())) {
                sb.append("The following Backup Jobs with ID(s) " + jobIDs
                        + " were successfully Waited on.");
                this.handleRefresh();
            } else {
                sb.append("The following Backup Jobs with ID(s) " + jobIDs
                        + " failed to be Waited on.");
                statusHandler.error(
                        "Error in BackupSvcIncomingResponse From Edex:",
                        response.getExceptions());
            }
            statusMsgText = sb.toString();
            updateStatusText();
        } else {
            statusHandler.error(
                    "Select an item with 'New' or 'Wait' status to Wait on.");
            statusMsgText = "Select an item with 'New' or 'Wait' status to Wait on.";
            updateStatusText();
        }
    }

    @Override
    protected String[] getTableColumns() {
        return VizBackupServicesConstants.INCOMING_TABLE_COLUMNS;
    }

    @Override
    protected void populateTable() {
        if (table != null && !table.isDisposed()) {
            // Clear the tree
            table.removeAll();
            for (BackupSvcIncoming incomingJob : incomingJobs) {
                TableItem item = new TableItem(table, SWT.NULL);
                item.setText(0, Long.toString(incomingJob.getBksvcId()));
                item.setText(1, incomingJob.getComponent());
                item.setText(2,
                        incomingJob.getBackupSvcJobInfo().getJobFilePath());
                item.setText(3, incomingJob.getRecipientSite());
                item.setText(4, incomingJob.getSenderSite());
                item.setText(5, incomingJob.getSystemVersion());
                item.setText(6, incomingJob.getSenderVersion());
                Date updateTime = new Date(incomingJob.getUpdateTime());
                item.setText(7, dateTimeFormatter.format(updateTime));
                item.setText(8, incomingJob.getStatus());
                item.setText(9,
                        incomingJob.getBackupSvcJobInfo().getJobFileLocation());
                item.setData(incomingJob);
            }
            packColumns();
        }
    }

    @Override
    protected void sortColumnData(String title) {
        sortComparator.setSortText(title);
        Collections.sort(incomingJobs, sortComparator);
        if (reverseSort) {
            Collections.reverse(incomingJobs);
        }
        populateTable();
    }

    /**
     * Comparator for sorting the rows in the tree.
     */
    private class IncomingSortComparator
            implements Comparator<BackupSvcIncoming> {
        private String sortText;

        @Override
        public int compare(BackupSvcIncoming o1, BackupSvcIncoming o2) {
            String[] incomingColumns = VizBackupServicesConstants.INCOMING_TABLE_COLUMNS;
            int value = 0;
            if (incomingColumns[0].equals(sortText)) {
                Long id1 = o1.getBksvcId();
                Long id2 = o2.getBksvcId();
                value = id1.compareTo(id2);
            } else if (incomingColumns[1].equals(sortText)) {
                value = o1.getComponent().compareTo(o2.getComponent());
            } else if (incomingColumns[9].equals(sortText)) {
                value = o1.getBackupSvcJobInfo().getJobFileLocation().compareTo(
                        o2.getBackupSvcJobInfo().getJobFileLocation());
            } else if (incomingColumns[2].equals(sortText)) {
                value = o1.getBackupSvcJobInfo().getJobFilePath()
                        .compareTo(o2.getBackupSvcJobInfo().getJobFilePath());
            } else if (incomingColumns[3].equals(sortText)) {
                value = o1.getRecipientSite().compareTo(o2.getRecipientSite());
            } else if (incomingColumns[4].equals(sortText)) {
                value = o1.getSenderSite().compareTo(o2.getSenderSite());
            } else if (incomingColumns[5].equals(sortText)) {
                value = o1.getSystemVersion().compareTo(o2.getSystemVersion());
            } else if (incomingColumns[6].equals(sortText)) {
                value = o1.getSenderVersion().compareTo(o2.getSenderVersion());
            } else if (incomingColumns[7].equals(sortText)) {
                Date d1 = new Date(o1.getUpdateTime());
                Date d2 = new Date(o2.getUpdateTime());
                value = d1.compareTo(d2);
            } else if (incomingColumns[8].equals(sortText)) {
                value = o1.getStatus().compareTo(o2.getStatus());
            } else {
                value = 0;
            }
            return value;
        }

        public void setSortText(String sortText) {
            this.sortText = sortText;
        }
    }

    @Override
    protected void queryJobs() {
        incomingJobs = new ArrayList<>();
        BackupSvcIncomingResponse response = null;
        IncomingBackupJobsRequest request = null;
        // get the SQL string
        boolean isOutgoing = false;
        String queryStr = BSJFilterMemo.toSQLStatement(isOutgoing);
        request = new IncomingBackupJobsRequest(
                IncomingOutgoingJobsRequest.RequestType.READ,
                LocalizationManager.getInstance().getSite(), queryStr);
        try {
            response = (BackupSvcIncomingResponse) RequestRouter.route(request);
            incomingJobs.addAll(response.getIncomingJobs());
        } catch (Exception e) {
            statusHandler.error(
                    "Unable to send request to server to get Incoming Backup Service Jobs.",
                    response.getExceptions());
        }
    }

