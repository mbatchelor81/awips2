    /**
     * Handles a file being rejected at the recipient site.
     */
    protected void handleReject() {
        BackupSvcIncomingResponse response = null;
        IncomingBackupJobsRequest request = null;
        List<Long> jobIDs = new ArrayList<>();
        List<Long> outJobIDs = new ArrayList<>();
        String jobStatus;
        // get all the incoming and outgoing job id's and store it into a list
        for (TableItem selectedTableItem : getSelectedTableItems()) {

            jobStatus = ((BackupSvcIncoming) selectedTableItem.getData())
                    .getStatus();
            // if a job has not been Accepted it can be rejected
            if (!jobStatus.equalsIgnoreCase(DbStatus.ACCEPTED.toString())) {
                jobIDs.add(((BackupSvcIncoming) selectedTableItem.getData())
                        .getBksvcId());
                outJobIDs.add(((BackupSvcIncoming) selectedTableItem.getData())
                        .getOutBksvcId());
            }
        }
        /*
         * If items are not selected don't make a request to server, instead
         * prompt the user to make selections
         */
        if (!(jobIDs.isEmpty() && outJobIDs.isEmpty())) {
            request = new IncomingBackupJobsRequest(
                    IncomingOutgoingJobsRequest.RequestType.REJECTED,
                    LocalizationManager.getInstance().getSite(), jobIDs,
                    outJobIDs);
            try {
                response = (BackupSvcIncomingResponse) RequestRouter
                        .route(request);
            } catch (Exception e) {
                statusHandler.error(
                        "Unable to send request to server to Reject Incoming Backup Jobs.",
                        e);
            }
            // Update statusMsgText
            StringBuilder sb = new StringBuilder();
            if (response == null) {
                sb.append("The following Backup Jobs with ID(s) " + jobIDs
                        + " failed to be Rejected. Null Response From Edex");
            }
            // check the response object passed to CAVE for exceptions
            else if ((response.getExceptions() == null
                    || response.getExceptions().getMessage().isEmpty())) {
                // handle partial Backup job status update
                if (response.getFailedIncomingJobs() != null) {
                    List<Long> failedJobids = new ArrayList<>();
                    for (BackupSvcIncoming Job : response
                            .getFailedIncomingJobs()) {
                        failedJobids.add(Job.getBksvcId());
                    }
                    sb.append("Not all Backup Jobs were successfully Rejected."
                            + "The following Backup Jobs with ID(s) "
                            + failedJobids + " failed to be Rejected.");
                    statusHandler.error(
                            "Error in BackupSvcIncomingResponse From Edex:",
                            response.getExceptions());
                    this.handleRefresh();
                    // all Backup jobs are successfully updated
                } else {
                    sb.append("The following Backup Jobs with ID(s) " + jobIDs
                            + " were successfully Rejected.");
                    this.handleRefresh();
                }
            } else {
                sb.append("The following Backup Jobs with ID(s) " + jobIDs
                        + " failed to be Rejected.");
                statusHandler.error(
                        "Error in BackupSvcIncomingResponse From Edex: ",
                        response.getExceptions());
            }
            statusMsgText = sb.toString();
            updateStatusText();
        } else {
            statusHandler
                    .error("Select an item without Accepted status to Reject.");
            statusMsgText = "Select an item without Accepted status to Reject.";
            updateStatusText();
        }
    }

