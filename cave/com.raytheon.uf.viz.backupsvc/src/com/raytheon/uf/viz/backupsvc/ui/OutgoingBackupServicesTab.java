    /**
     * Handles sending a file to the configured hosts.
     */
    protected void handleSend() {
        BackupSvcOutgoingResponse response = null;
        OutgoingBackupJobsRequest request = null;
        String jobStatus;
        List<Long> jobIDs = new ArrayList<>();
        // get all the outgoing job id's and store it into a list
        for (TableItem selectedTableItem : getSelectedTableItems()) {

            jobStatus = ((BackupSvcOutgoing) selectedTableItem.getData())
                    .getStatus();
            /*
             * All BSJ jobs with “new”/"wait" status in outgoing tab are sent to
             * recipient sites.
             */
            if (jobStatus.equalsIgnoreCase(DbStatus.WAIT.toString())
                    || jobStatus.equalsIgnoreCase(DbStatus.NEW.toString())
                    || jobStatus.equalsIgnoreCase(DbStatus.FAILED.toString())) {
                jobIDs.add(((BackupSvcOutgoing) selectedTableItem.getData())
                        .getBksvcId());
            }
        }
        /*
         * If items are not selected don't make a request to server, instead
         * prompt the user to make selections
         */
        if (!(jobIDs.isEmpty())) {
            request = new OutgoingBackupJobsRequest(
                    IncomingOutgoingJobsRequest.RequestType.SEND,
                    LocalizationManager.getInstance().getSite(), jobIDs);
            try {
                response = (BackupSvcOutgoingResponse) RequestRouter
                        .route(request);
            } catch (Exception e) {
                statusHandler.error(
                        "Unable to request server to send Outgoing Backup Service Jobs.",
                        e);
            }
            // Update statusMsgText
            StringBuilder sb = new StringBuilder();
            if (response == null) {
                sb.append("The following Backup Jobs with ID(s) " + jobIDs
                        + " failed to be sent to recipient sites. Null Response From Edex");
            }
            // check the response object passed to cave for exceptions
            else if ((response.getExceptions() == null
                    || response.getExceptions().getMessage().isEmpty())
                    && (response.getFailedOutgoingJobs() == null
                    || response.getFailedOutgoingJobs().isEmpty())) {
                sb.append("The following Backup Jobs with ID(s) " + jobIDs
                        + " were successfully sent to recipient sites.");
                this.handleRefresh();
            } else {
                sb.append("There were failures sending the job(s).");
                if (response.getExceptions() != null) {
                    statusHandler.error(
                            "Error in BackupSvcIncomingResponse From Edex:",
                            response.getExceptions());
                }

                if (response.getFailedOutgoingJobs() != null) {
                    response.getFailedOutgoingJobs()
                            .forEach(
                                    (job, exception) -> statusHandler.error(
                                            "Failed to send job with ID "
                                                    + job.getBksvcId(),
                                            exception));
                }
            }
            statusMsgText = sb.toString();
            updateStatusText();
        } else {
            statusHandler
                    .error("Select an item with New or Wait status to Send.");
            statusMsgText = "Select an item with New or Wait status to Send.";
            updateStatusText();
        }
    }

