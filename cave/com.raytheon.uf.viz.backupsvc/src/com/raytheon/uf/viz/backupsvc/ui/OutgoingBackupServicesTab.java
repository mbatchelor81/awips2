    protected void handleWait() {
        BackupSvcOutgoingResponse response = null;
        OutgoingBackupJobsRequest request = null;
        List<Long> jobIDs = new ArrayList<>();
        String jobStatus;
        // get all the outgoing job id's and store it into a list
        for (TableItem selectedTableItem : getSelectedTableItems()) {

            jobStatus = ((BackupSvcOutgoing) selectedTableItem.getData())
                    .getStatus();
            // Only review Jobs with "new" or "wait" status
            if (jobStatus.equalsIgnoreCase(DbStatus.WAIT.toString())
                    || jobStatus.equalsIgnoreCase(DbStatus.NEW.toString())
                    || jobStatus.equalsIgnoreCase(DbStatus.FAILED.toString())) {
                jobIDs.add(((BackupSvcOutgoing) selectedTableItem.getData())
                        .getBksvcId());
            }
        }
        /*
         * If items are not selected don't make a request to server, instead
         * prompt the user to make selections
         */
        if (!(jobIDs.isEmpty())) {
            request = new OutgoingBackupJobsRequest(
                    IncomingOutgoingJobsRequest.RequestType.WAIT,
                    LocalizationManager.getInstance().getSite(), jobIDs);
            try {
                response = (BackupSvcOutgoingResponse) RequestRouter
                        .route(request);
            } catch (Exception e) {
                statusHandler.error(
                        "Unable to send request to server to Wait on Outgoing Backup Jobs.",
                        e);
            }
            // Update statusMsgText
            StringBuilder sb = new StringBuilder();
            if (response == null) {
                sb.append("The following Backup Jobs with ID(s) " + jobIDs
                        + " failed to be Waited on. Null Response From Edex");
            }
            /*
             * Check the response object passed to CAVE for exceptions and
             * OutgoingJob list is not empty
             */
            else if (response.getExceptions() == null
                    || response.getExceptions().getMessage().isEmpty()
                            && !response.getOutgoingJobs().isEmpty()) {
                sb.append("The following Backup Jobs with ID(s) " + jobIDs
                        + " were successfully Waited on.");
                this.handleRefresh();
            } else {
                sb.append("The following Backup Jobs with ID(s) " + jobIDs
                        + " failed to be Waited on.");
                statusHandler.error("Error Response From Edex:",
                        response.getExceptions());
            }
            statusMsgText = sb.toString();
            updateStatusText();
        } else {
            statusHandler.error(
                    "Select an item with 'New' or 'Wait' status to Wait on.");
            statusMsgText = "Select an item with 'New' or 'Wait' status to Wait on.";
            updateStatusText();
        }
    }

    /**
     * Handles deleting/removing the selected BackupSvcOutgoing jobs.
     */
    protected void handleDelete() {
        BackupSvcOutgoingResponse response = null;
        OutgoingBackupJobsRequest request = null;
        String jobStatus;
        List<Long> jobIDs = new ArrayList<>();
        // get all the outgoing job id's and store it into a list
        for (TableItem selectedTableItem : getSelectedTableItems()) {

            jobStatus = ((BackupSvcOutgoing) selectedTableItem.getData())
                    .getStatus();
            // if a job has not been Sent delete this job from outgoing table
            if (jobStatus.equalsIgnoreCase(DbStatus.WAIT.toString())
                    || jobStatus.equalsIgnoreCase(DbStatus.NEW.toString())
                    || jobStatus.equalsIgnoreCase(DbStatus.FAILED.toString())) {
                jobIDs.add(((BackupSvcOutgoing) selectedTableItem.getData())
                        .getBksvcId());
            }
        }
        /*
         * If items are not selected don't make a request to server, instead
         * prompt the user to make selections
         */
        if (!(jobIDs.isEmpty())) {
            request = new OutgoingBackupJobsRequest(
                    IncomingOutgoingJobsRequest.RequestType.DELETE,
                    LocalizationManager.getInstance().getSite(), jobIDs);
            try {
                response = (BackupSvcOutgoingResponse) RequestRouter
                        .route(request);
            } catch (Exception e) {
                statusHandler.error(
                        "Unable to send request to server to delete Outgoing Backup Jobs.",
                        e);
            }
            // Update statusMsgText
            StringBuilder sb = new StringBuilder();
            // check the response object passed to cave for exceptions
            if (response == null) {
                sb.append("The following Backup Jobs with ID(s) " + jobIDs
                        + " failed to be Deleted. Null Response From Edex");
            } else if ((response.getExceptions() == null
                    || response.getExceptions().getMessage().isEmpty())) {
                sb.append("The following Backup Jobs with ID(s) " + jobIDs
                        + " were successfully Deleted.");
                this.handleRefresh();
            } else {
                sb.append("The following Backup Jobs with ID(s) " + jobIDs
                        + " failed to be Deleted.");
                statusHandler.error("Error Response From Edex:",
                        response.getExceptions());
            }
            statusMsgText = sb.toString();
            updateStatusText();
        } else {
            statusHandler.error(
                    "Select an item without Sent,Accepted or Rejected status to Delete.");
            statusMsgText = "Select an item without Sent,Accepted or Rejected status to Delete.";
            updateStatusText();
        }
    }

