    @Override
    public void populateTable() {
        if (table != null && !table.isDisposed()) {
            // Clear the tree
            table.removeAll();
            for (BackupSvcOutgoing outgoingJob : outgoingJobs) {
                TableItem item = new TableItem(table, SWT.NULL);
                item.setText(0, Long.toString(outgoingJob.getBksvcId()));
                item.setText(1, outgoingJob.getComponent());
                item.setText(2,
                        outgoingJob.getBackupSvcJobInfo().getJobFilePath());
                item.setText(3, outgoingJob.getSenderSite());
                item.setText(4, outgoingJob.getRecipientSite());
                item.setText(5, outgoingJob.getSystemVersion());
                item.setText(6, outgoingJob.getRecipientVersion());
                Date updateTime = new Date(outgoingJob.getUpdateTime());
                item.setText(7, dateTimeFormatter.format(updateTime));
                item.setText(8, outgoingJob.getStatus());
                item.setText(9,
                        outgoingJob.getBackupSvcJobInfo().getJobFileLocation());
                item.setData(outgoingJob);
                if (!isCompatibleEdexVersion(outgoingJob.getSystemVersion(),
                        outgoingJob.getRecipientVersion())) {
                    item.setBackground(
                            Display.getCurrent().getSystemColor(SWT.COLOR_RED));
                }
            }
            packColumns();
        }
    }

    @Override
    protected void sortColumnData(String title) {
        sortComparator.setSortText(title);
        Collections.sort(outgoingJobs, sortComparator);
        if (reverseSort) {
            Collections.reverse(outgoingJobs);
        }
        populateTable();
    }

    /**
     * check if a recipient Edex version is compatible with sender Edex version
     */
    private boolean isCompatibleEdexVersion(String senderEdexVersion,
            String recipientEdexVersion) {
        boolean retVal = false;
        if (recipientEdexVersion == null || recipientEdexVersion.isEmpty()
                || senderEdexVersion == null || senderEdexVersion.isEmpty()) {
            return retVal;
        }
        if (recipientEdexVersion.equalsIgnoreCase(senderEdexVersion)) {
            retVal = true;
        }
        return retVal;
    }

    /**
     * Comparator for sorting the rows in the table.
     */
    private class OutgoingSortComparator
            implements Comparator<BackupSvcOutgoing> {
        private String sortText;

        @Override
        public int compare(BackupSvcOutgoing o1, BackupSvcOutgoing o2) {
            String[] outgoingColumns = VizBackupServicesConstants.OUTGOING_TABLE_COLUMNS;
            int value = 0;
            if (outgoingColumns[0].equals(sortText)) {
                Long id1 = o1.getBksvcId();
                Long id2 = o2.getBksvcId();
                value = id1.compareTo(id2);
            } else if (outgoingColumns[1].equals(sortText)) {
                value = o1.getComponent().compareTo(o2.getComponent());
            } else if (outgoingColumns[9].equals(sortText)) {
                value = o1.getBackupSvcJobInfo().getJobFileLocation().compareTo(
                        o2.getBackupSvcJobInfo().getJobFileLocation());
            } else if (outgoingColumns[2].equals(sortText)) {
                value = o1.getBackupSvcJobInfo().getJobFilePath()
                        .compareTo(o2.getBackupSvcJobInfo().getJobFilePath());
            } else if (outgoingColumns[3].equals(sortText)) {
                value = o1.getSenderSite().compareTo(o2.getSenderSite());
            } else if (outgoingColumns[4].equals(sortText)) {
                value = o1.getRecipientSite().compareTo(o2.getRecipientSite());
            } else if (outgoingColumns[5].equals(sortText)) {
                value = o1.getSystemVersion().compareTo(o2.getSystemVersion());
            } else if (outgoingColumns[6].equals(sortText)) {
                value = o1.getRecipientVersion()
                        .compareTo(o2.getRecipientVersion());
            } else if (outgoingColumns[7].equals(sortText)) {
                Date d1 = new Date(o1.getUpdateTime());
                Date d2 = new Date(o2.getUpdateTime());
                value = d1.compareTo(d2);
            } else if (outgoingColumns[8].equals(sortText)) {
                value = o1.getStatus().compareTo(o2.getStatus());
            } else {
                value = 0;
            }
            return value;
        }

        public void setSortText(String sortText) {
            this.sortText = sortText;
        }
    }

    @Override
    protected void queryJobs() {
        outgoingJobs = new ArrayList<>();
        BackupSvcOutgoingResponse response = null;
        OutgoingBackupJobsRequest request = null;
        // get the SQL string
        boolean isOutgoing = true;
        String queryStr = BSJFilterMemo.toSQLStatement(isOutgoing);
        request = new OutgoingBackupJobsRequest(
                IncomingOutgoingJobsRequest.RequestType.READ,
                LocalizationManager.getInstance().getSite(), queryStr);
        try {
            response = (BackupSvcOutgoingResponse) RequestRouter.route(request);
            outgoingJobs.addAll(response.getOutgoingJobs());
        } catch (Exception e) {
            statusHandler.error(
                    "Unable to send request to server to get a list of outgoing BackupJobs.",
                    response.getExceptions());
        }
    }

    /**
     * Displays the currently selected table item in a file browser window.
     * Allow the actual file to viewed.
     */
    protected void handleReview() {
        List<BackupSvcJobInfo> infos = new ArrayList<>();

        for (TableItem item : getSelectedTableItems()) {
            BackupSvcOutgoing outgoingJob = (BackupSvcOutgoing) item.getData();
            infos.add(outgoingJob.getBackupSvcJobInfo());
        }

        if (!infos.isEmpty()) {
            if (fileViewerDialog == null || fileViewerDialog.isDisposed()) {
                fileViewerDialog = new BackupServicesFileViewerDialog(
                        tab.getParent().getShell(), infos);
                fileViewerDialog.open();
            } else {
                fileViewerDialog.close();
                fileViewerDialog = new BackupServicesFileViewerDialog(
                        tab.getParent().getShell(), infos);
                fileViewerDialog.open();
            }
        } else {
            statusHandler.error(
                    "Please make a selection to review a Backup Service Job item! ");
            statusMsgText = "Please make a selection to review a Backup Service Job item! ";
            updateStatusText();
        }
    }
